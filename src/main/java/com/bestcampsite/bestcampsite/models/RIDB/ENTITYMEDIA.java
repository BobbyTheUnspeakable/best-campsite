package com.bestcampsite.bestcampsite.models.RIDB;

public class ENTITYMEDIA {

    private transient String Credits;

    private transient String Description;

    private transient String EmbedCode;

    private transient String EntityID;

    private transient String EntityMediaID;

    private String EntityType;

    private transient String Height;

    private String MediaType;

    private transient String Subtitle;

    private transient String Title;

    private String URL;

    private transient String Width;

    public ENTITYMEDIA() {
    }

    public String getCredits() {
        return Credits;
    }

    public void setCredits(String credits) {
        Credits = credits;
    }

    public String getDescription() {
        return Description;
    }

    public void setDescription(String description) {
        Description = description;
    }

    public String getEmbedCode() {
        return EmbedCode;
    }

    public void setEmbedCode(String embedCode) {
        EmbedCode = embedCode;
    }

    public String getEntityID() {
        return EntityID;
    }

    public void setEntityID(String entityID) {
        EntityID = entityID;
    }

    public String getEntityMediaID() {
        return EntityMediaID;
    }

    public void setEntityMediaID(String entityMediaID) {
        EntityMediaID = entityMediaID;
    }

    public String getEntityType() {
        return EntityType;
    }

    public void setEntityType(String entityType) {
        EntityType = entityType;
    }

    public String getHeight() {
        return Height;
    }

    public void setHeight(String height) {
        Height = height;
    }

    public String getMediaType() {
        return MediaType;
    }

    public void setMediaType(String mediaType) {
        MediaType = mediaType;
    }

    public String getSubtitle() {
        return Subtitle;
    }

    public void setSubtitle(String subtitle) {
        Subtitle = subtitle;
    }

    public String getTitle() {
        return Title;
    }

    public void setTitle(String title) {
        Title = title;
    }

    public String getURL() {
        return URL;
    }

    public void setURL(String URL) {
        this.URL = URL;
    }

    public String getWidth() {
        return Width;
    }

    public void setWidth(String width) {
        Width = width;
    }
}
